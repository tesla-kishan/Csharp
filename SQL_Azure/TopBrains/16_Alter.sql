-- ZIPCODE_INFO
CREATE TABLE ZIPCODE_INFO (
    ZIP_CODE VARCHAR(10),
    CITY VARCHAR(50),
    STATE VARCHAR(10)
);

-- INSTRUCTOR_INFO
CREATE TABLE INSTRUCTOR_INFO (
    INSTRUCTOR_ID INT,
    INSTRUCTOR_FIRST_NAME VARCHAR(50),
    INSTRUCTOR_LAST_NAME VARCHAR(50),
    STREET_ADDRESS VARCHAR(100),
    ZIP_CODE VARCHAR(10)
);

-- COURSE_INFO
CREATE TABLE COURSE_INFO (
    COURSE_NO INT,
    COURSE_NAME VARCHAR(100),
    COURSE_PREREQUISITE INT,
    COST DECIMAL(10,2)
);

-- STUDENT_INFO
CREATE TABLE STUDENT_INFO (
    STUDENT_ID INT,
    STUDENT_FIRST_NAME VARCHAR(50),
    STUDENT_LAST_NAME VARCHAR(50),
    STREET_ADDRESS VARCHAR(100),
    ZIP_CODE VARCHAR(10)
);

-- SECTION_INFO
CREATE TABLE SECTION_INFO (
    SECTION_ID INT,
    COURSE_NO INT,
    SECTION_NO INT,
    INSTRUCTOR_ID INT,
    LOCATION VARCHAR(100),
    CAPACITY INT
);

-- ENROLLMENT_INFO
CREATE TABLE ENROLLMENT_INFO (
    STUDENT_ID INT,
    SECTION_ID INT,
    ENROLLMENT_DATE DATETIME
);

-- GRADE_INFO
CREATE TABLE GRADE_INFO (
    STUDENT_ID INT,
    SECTION_ID INT,
    GRADE_TYPE_CODE CHAR(5),
    GRADE_CODE_OCCURANCE BIGINT,
    NUMERIC_GRADE INT
);



